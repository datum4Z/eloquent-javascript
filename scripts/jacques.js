function phi(table) {
  let numerator = table[3] * table[0] - table[2] * table[1];
  let denominator = Math.sqrt(
    (table[2] + table[3]) *
    (table[0] + table[1]) *
    (table[1] + table[3]) *
    (table[0] + table[2])
  );

  return numerator / denominator;
}

